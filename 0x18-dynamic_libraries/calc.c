#include "main.h"

/*
 * add - adds number
 * @a: input
 * @b: input
 * Return: returns the sum of a and b
 */
int add(int a, int b)
{
	return (a + b);
}


/*
 * mul -multiplies number
 * @a: input
 * @b: input
 * Return: returns the product of a and b
 */
int mul(int a, int b)
{
	return (a * b);
}

/*
 * sub - substracts  number
 * @a: input
 * @b: input
 * Return: returns the difference of a and b
 */
int sub(int a, int b)
{
	return (a - b);
}

/*
 * mod - adds number
 * @a: input
 * @b: input
 * Return: returns the modula of a and b
 */
int mod(int a, int b)
{
	return (a % b);
}

/*
 * div -  number
 * @a: input
 * @b: input
 * Return: returns the division of a and b
 */
int div(int a, int b)
{
	return (a / b);
}
